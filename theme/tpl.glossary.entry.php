<p class="just">A French ballet term, for the wooden bar, either attached to the walls of a studio (usually mirrored) or free standing if backstage, used for stage dancers to warm up before a musical or a drama performance requiring choreographed movement. In the case of ballet, dancers use the barre for two-thirds of their obligatory daily company class. It is used for balance and bodily adjustment and alignment (also stretching the muscles) while warming up the various muscle groups within the body with preparatory exercises. In the case of a modern dance production, it is most unlikely that a barre would be used for preparation and warm up, but for general stage dancing, including tap, one is always readily available.</p>

