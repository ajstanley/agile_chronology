<h2 id="glossary_heading">BAC</h2>
<p class="just">Battersea Arts Centre. London performance centre for experimental fringe and avant-garde theatre.</p>

<h2 id="glossary_heading">Backcloth/backdrop</h2>
<p class="just">A large canvas cloth hung at the back of the stage from a grid.  The canvas is frequently painted and lights are projected onto the drop during performance. While common in Victorian theatres, it is more likely today to be found in opera and ballet productions.</p>

<h2 id="glossary_heading">Backing</h2>
<p class="just">A piece of cloth used to hide anything that may be seen through an opening in the set.</p>

<h2 id="glossary_heading">Back Stage</h2>
<p class="just">The area behind the performance space, including the wings, dressing rooms, workshops, which remains invisible from the auditorium.</p>

<h2 id="glossary_heading">Bag</h2>
<p class="just">A sand bag used as a counterweight for a suspended prop or piece of scenery.</p>

