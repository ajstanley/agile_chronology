<h2 id="glossary_heading">Bauprobe</h2>
<p class="just">Term used for a try out of a scene on a temporary set to see how everything works.</p>

<h2 id="glossary_heading">Bedroom Farce</h2>
<p class="just">Popular nineteenth- and twentieth-century form of comedy, originating in France, with Feydeau as its prime exponent. The term derives from its theme, which focuses on couples appearing to be engaged in extra-marital affairs. Thus, the setting is primarily a bedroom. The dramatic situation is frequently ridiculous with mistaken identity and revolving door exits and entrances. During the 1960s in England, this comic form was made popular by Brian Rix (see Whitehall Farce). Today, Alan Ayckbourn is the chief exponent of this style of comedy, including his play Bedroom Farce. </p>

